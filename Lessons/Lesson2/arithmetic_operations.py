age = 22

print(age)
age = 23

print(age)

"""
docstring / multiline comment

Arithmetic operators:
+   - addition
-   - subtraction
/   - division
*   - multiplication
//  - floor division
**  - power
%   - modulus
"""

print('just addition', age + 1)  # 24
print('just subtraction', age - 1)  # 22
age = age + 1  # 23 + 1
print('after reassignment', age)  # 24

print('just division', age / 2)  # 12
print('just multiplication', age * 2)  # 48
print('floor division', age // 2)  # 12
print('power', age ** 2)  # 576
print('modulus', age % 2)  # 0, 1

